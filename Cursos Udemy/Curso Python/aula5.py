#Variáveis segunda parte

"""
Variáveis seguem uma sequência
Nome da variável = valor da variável

TEXTOS = string str
NÚMEROS = inteiros int = sem casa decimal
Frações = float = com casa decimal
"""

texto = "este é valor da variável texto"
print(texto)
print(type(texto))
num = 10 #valor da variável é 10
print("O valor da variável num é:")
print(num)
print(type(num))

frac = 10.55
print("O valor da variável frac é:")
print(frac)
print(type(frac))
