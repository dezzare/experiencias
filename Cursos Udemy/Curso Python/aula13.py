#Operadores comparativos, ou relacionais

"""
Operadores de Igualdade
Igualdade: ==
Diferente: !=
"""

"""
Operadores Diferenciais
Maior que: >
Menor que: <
Maior ou igual: >=
Menor ou igual: <=
"""

print('a'=='b') #Falso
print('a'=='a') #Verdadeiro
print('a'<='b') #Verdadeiro
print('a'<'A') #Falso
print('a'<='A') #Falso
print('a'>'A') #Verdadeiro
